    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String phoneNumber = phone_no.getText().trim();
        if (!phoneNumber.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(null, "Not a valid 10-digit phone number.");
            user.setText("");
            email_id.setText("");
            phone_no.setText("");
            pass.setText("");
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/travel_agency", "root", "WARmachineROXXX");
            String sql = "INSERT INTO user_account (Username, Email_id, Phone_number, Password) VALUES (?, ?, ?, ?)";
            PreparedStatement ptst = con.prepareStatement(sql);
            ptst.setString(1, user.getText());
            ptst.setString(2, email_id.getText());
            ptst.setString(3, phone_no.getText());
            ptst.setString(4, pass.getText());
            ptst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Account created successfully");
            new HomePage().setVisible(true);
            dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

